    <script>
        $(document).on('click','.pagination a',function(e){
                e.preventDefault();
                var page = $(this).attr('href').split('page=')[1];
                // fetch_data(page);
                filter_data(page);
            });
        function filter_data(page){
            var action = 'fetch_data';
            // var page = $('.pagination a').attr('href').split('page=')[1];
            // fetch_data(page);
            var min = $('#hidden_minimum_price').val();
            var max = $('#hidden_maximum_price').val();
            var cat = get_filter('cat');
            var brand = get_filter('brand');
            $.ajax({
                    url:"{{route('website.filter')}}"+"?page="+page,
                    type:"GET",
                    data:{action:action,cat:cat,brand:brand,min:min,max:max},
                    success:function(data){
                        $("#load_data").html(data);
                    }
                }); 
            }
            //get filter function 
            function get_filter(class_name)
            {
                var filter = [];
                $('.'+class_name+':checked').each(function(){
                    filter.push($(this).val());
                });
                return filter;
            }
            //action function 
            $('.common_selector').click(function(){
                filter_data();
                //alert('ok');
            });
        // range slider
        $('#price_range').slider({
                range:true,
                min:100,
                max:5000,
                values:[100, 5000],
                step:300,
                stop:function(event, ui)
                {
                    $('#price_show').html(ui.values[0] + ' - ' + ui.values[1]);
                    $('#hidden_minimum_price').val(ui.values[0]);
                    $('#hidden_maximum_price').val(ui.values[1]);
                    filter_data();
                }
            });

    </script>
